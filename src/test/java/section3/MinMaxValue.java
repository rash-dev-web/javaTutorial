package section3;

public class MinMaxValue {
	
	public static void main(String[] args) {
		
		byte minValue = Byte.MIN_VALUE;
		byte maxValue = Byte.MAX_VALUE;
		System.out.println("byte minValue: "+ minValue);
		System.out.println("byte maxValue: "+ maxValue);
		
		short sminValue = Short.MIN_VALUE;
		short smaxValue = Short.MAX_VALUE;
		System.out.println("short minValue: "+ sminValue);
		System.out.println("short maxValue: "+ smaxValue);
		
		int iminValue = Integer.MIN_VALUE;
		int imaxValue = Integer.MAX_VALUE;
		System.out.println("int minValue: "+ iminValue);
		System.out.println("int maxValue: "+ imaxValue);
		
		long lminValue = Long.MIN_VALUE;
		long lmaxValue = Long.MAX_VALUE;
		System.out.println("long minValue: "+ lminValue);
		System.out.println("long maxValue: "+ lmaxValue);
	}

}
